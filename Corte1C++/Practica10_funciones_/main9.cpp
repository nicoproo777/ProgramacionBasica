#include <iostream>
using namespace std;

int sumar(int a, int b){
	
	int r=a+b;
	return r;
	
}

int restar(int h, int x){
	
	int d=h-x;
	return d;
	
}

int multiplicar(int p, int q){
	
	int e=p*q;
	return e;
	
}	
int main(){

 int h=sumar(7,31);
 cout<<h<<endl;
 
 int z=restar(9,7);
 cout<<z<<endl;
 
 int l=multiplicar(8,8);
 cout<<l<<endl;
 
}
